def render_discovery_markdown() -> str:
    lines = [
        "# Dataset Discovery",
        "",
        "Generated by `python -m tracegate data discover`.",
        "",
        "## Selection Summary",
        "",
        "Final selected source: GitHub REST API Pull Requests. The P0 path uses public Pull Request metadata and changed-file provenance directly from GitHub, with no mock, synthetic, or fallback records.",
        "",
        "## Candidates",
        "",
    ]
    for row in DISCOVERY_ROWS:
        lines.extend(
            [
                f"### {row['name']}",
                "",
                f"- Official URL / dataset URL: {row['url']}",
                f"- Data fields: {row['fields']}",
                f"- License / use restrictions: {row['license']}",
                f"- GitHub token required: {row['token']}",
                f"- Extra dependencies: {row['dependencies']}",
                f"- Download method: {row['download']}",
                f"- Sample scale: {row['sample_size']}",
                f"- P0 decision: {row['decision']}",
                "",
            ]
        )
    lines.extend(
        [
            "## Sampling Strategy",
            "",
            "- Fetch recent closed Pull Requests from a small fixed list of public repositories.",
            "- Keep only merged Pull Requests with changed-file provenance.",
            "- Normalize each merged PR into a claim/evidence case with `source_dataset=github_api`.",
            "- Mark low-provenance records as `needs_manual_review` and exclude them from scored metrics.",
            "",
            "## Cache Policy",
            "",
            "- Raw API responses are written to `datasets/real_min/raw/`, which is ignored by Git.",
            "- Normalized real cases may be committed only when they are small, public, and provenance-linked.",
            "- The manifest records sha256 for the normalized dataset.",
            "- If fetch fails or produces fewer than 8 scored real cases, no fallback data is generated.",
            "",
            "## Cache Verification",
            "",
            "A cache is considered real only when it contains public GitHub URLs, merge commit or PR provenance, at least one evidence item per scored case, and a manifest hash matching the dataset used by the run.",
            "",
        ]
    )
    return "\n".join(lines)


def write_discovery_doc(path: Path = Path("docs/DATASET_DISCOVERY.md")) -> Path:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(render_discovery_markdown(), encoding="utf-8")
    return path
